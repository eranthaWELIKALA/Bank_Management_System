    public void setUserAccountInfo(String acc) {
        this.acc = acc;
        
        jTextField12.setText(acc);
        String query = "SELECT * FROM `account` WHERE Acc=?";
        PreparedStatement pst;
        try {
            pst = this.connect.prepareStatement(query);
            pst.setString(1, this.acc);
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                jTextField1.setText(rs.getString("Name"));
                jTextField2.setText(rs.getString("DOB"));
                jTextField3.setText(rs.getString("Address"));
                jTextField4.setText(rs.getString("Gender"));
                jTextField5.setText(rs.getString("Nationality"));
                jTextField6.setText(rs.getString("Caste"));
                jTextField7.setText(rs.getString("Acc"));
                jTextField8.setText(rs.getString("Branch"));
                jTextField9.setText(rs.getString("Acc_Type"));
                jTextField10.setText(rs.getString("Mobile"));
                jTextField11.setText(rs.getString("Landline"));
                this.password = rs.getString("Password");
            }
            else{
                this.password = "";
            }
        } catch (SQLException ex) {
            Logger.getLogger(UserAccountPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

