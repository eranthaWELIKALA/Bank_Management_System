    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        ImageIcon icon = new ImageIcon(this.logo);
        if(Double.parseDouble(jTextField16.getText())<Double.parseDouble(jTextField19.getText())){
            JOptionPane.showMessageDialog(this,"Insufficient balance",this.title,JOptionPane.INFORMATION_MESSAGE,icon);
            jTextField19.setText("");
        }
        else{
            if(JOptionPane.showConfirmDialog(this,"Do you want to transfer rs."+jTextField19.getText()+"/= into "+jTextField21.getText()+"'s account("+jTextField18.getText()+") ?", this.title, JOptionPane.YES_NO_OPTION) != JOptionPane.YES_NO_OPTION)
            {

            }
            else
            {
                try {
                    String tAcc = jTextField18.getText();
                    double balance=0;
                    double tBalance=0;
                    double amount=0;
                    balance = Double.parseDouble(jTextField16.getText())+ balance;
                    amount = Double.parseDouble(jTextField19.getText())+ amount;

                    String query0 = "SELECT * FROM account WHERE ACC=?";
                    String query1 = "UPDATE account SET Balance=? WHERE Acc=?";
                    String query2 = "UPDATE balance SET Balance=? , Debit=? WHERE Acc=?";
                    String query3 = "UPDATE account SET Balance=? WHERE Acc=?";
                    String query4 = "UPDATE balance SET Balance=? , Credit=? WHERE Acc=?";

                    PreparedStatement pst0,pst1,pst2,pst3,pst4;
                    ResultSet rs0;

                    pst0 = this.connect.prepareStatement(query0);
                    pst1 = this.connect.prepareStatement(query1);
                    pst2 = this.connect.prepareStatement(query2);
                    pst3 = this.connect.prepareStatement(query3);
                    pst4 = this.connect.prepareStatement(query4);

                    pst0.setString(1,tAcc);
                    rs0 = pst0.executeQuery();
                    if(rs0.next()){
                        tBalance = Double.parseDouble(rs0.getString("Balance"))+tBalance+amount;
                        balance = balance - amount;

                        pst1.setDouble(1,balance);
                        pst1.setString(2,this.acc);
                        pst1.execute();

                        pst2.setDouble(1,balance);
                        pst2.setDouble(2,amount);
                        pst2.setString(3,this.acc);
                        pst2.execute();

                        pst3.setDouble(1,tBalance);
                        pst3.setString(2,tAcc);
                        pst3.execute();

                        pst4.setDouble(1,tBalance);
                        pst4.setDouble(2,amount);
                        pst4.setString(3,tAcc);
                        pst4.execute();

                        ImageIcon iconOK = new ImageIcon(this.logo);
                        JOptionPane.showMessageDialog(this,"Successfully transfered!",this.title,JOptionPane.INFORMATION_MESSAGE,iconOK);
                        jTextField17.setText("");
                        jTextField18.setText("");
                        jTextField19.setText("");
                        jTextField21.setText("");
                        jTextField20.setText(Double.toString(balance));
                        jTextField16.setText(Double.toString(balance));
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(UserAccountPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton8ActionPerformed

