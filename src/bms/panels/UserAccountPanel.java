    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        ImageIcon icon = new ImageIcon(this.logo);
        if(jPasswordField1.getText().equalsIgnoreCase("") || jPasswordField2.getText().equalsIgnoreCase("") || jPasswordField3.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this,"Please enter passwords",this.title,JOptionPane.INFORMATION_MESSAGE,icon);
        }
        else if(!jPasswordField1.getText().equals(this.password)){
            JOptionPane.showMessageDialog(this,"Incorrect password!",this.title,JOptionPane.ERROR_MESSAGE,icon);
        }
        else if(!jPasswordField2.getText().equals(jPasswordField3.getText())){
            JOptionPane.showMessageDialog(this,"New passwords are not matching!",this.title,JOptionPane.ERROR_MESSAGE,icon);
        }
        else{
            String query = "UPDATE account SET password=? WHERE Acc=?";
            try {
                PreparedStatement pst;
                pst = this.connect.prepareStatement(query);
                pst.setString(1,jPasswordField3.getText());
                pst.setString(2,this.acc);
                pst.execute();
                pst.close();
                JOptionPane.showMessageDialog(this,"Password is changed successfully!",this.title,JOptionPane.NO_OPTION,icon);
                jPasswordField1.setText("");
                jPasswordField2.setText("");
                jPasswordField3.setText("");

            } catch (SQLException ex) {
                Logger.getLogger(UserAccountPanel.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton6ActionPerformed

